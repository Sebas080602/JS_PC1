// Declaración de dos variables con números
const numero1 = 10;
const numero2 = 3;

// Aplicación de operadores aritméticos
const suma = numero1 + numero2;
const resta = numero1 - numero2;
const multiplicacion = numero1 * numero2;
const division = numero1 / numero2;
const modulo = numero1 % numero2;
const potencia = numero1 ** numero2;

// Mostrar los resultados en la consola
console.log(`Suma: ${numero1} + ${numero2} = ${suma}`); // Suma: 10 + 3 = 13
console.log(`Resta: ${numero1} - ${numero2} = ${resta}`); // Resta: 10 - 3 = 7
console.log(`Multiplicación: ${numero1} * ${numero2} = ${multiplicacion}`); // Multiplicación: 10 * 3 = 30
console.log(`División: ${numero1} / ${numero2} = ${division}`); // División: 10 / 3 = 3.333...
console.log(`Módulo: ${numero1} % ${numero2} = ${modulo}`); // Módulo: 10 % 3 = 1
console.log(`Potencia: ${numero1} ** ${numero2} = ${potencia}`); // Potencia: 10 ** 3 = 1000
